#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*num;
	if (n < 0)
	{
		write (fd, "-", 1);
		n *= -1;
	}
	num = ft_itoa(n);
	while (*num)
	{
		write (fd, num, 1);
		num++;
	}
}